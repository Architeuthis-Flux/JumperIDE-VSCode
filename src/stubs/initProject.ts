/** Build a valid, typed `jumperless.pyi` from the API reference + constants. */
export function generateStub(api: ApiRefData | null): string {
    const headings = api?.headings ?? [];
    const descriptions = api?.descriptions ?? {};

    const lines: string[] = [
        GENERATED_HEADER.trimEnd(),
        '"""Jumperless MicroPython API — type stubs for editor autocomplete."""',
        'from typing import Any',
        '',
    ];

    const emitted = new Set<string>();
    for (const heading of headings) {
        const fn = parseHeading(heading);
        if (!fn || emitted.has(fn.name)) { continue; }
        emitted.add(fn.name);

        const doc = descriptions[normalize(fn.name)];
        lines.push(`def ${fn.name}(${fn.params}) -> Any:`);
        if (doc) {
            lines.push(`    """${escapeDocstring(doc)}"""`);
        }
        lines.push('    ...');
        lines.push('');
    }

    // Constants / pin names / filesystem objects, so bare names resolve.
    const constants = getConstants();
    if (constants.length) {
        lines.push('# Constants and pin aliases injected by the firmware.');
        for (const c of constants) {
            if (isValidIdentifier(c) && !emitted.has(c)) {
                emitted.add(c);
                lines.push(`${c}: Any`);
            }
        }
        lines.push('');
    }

    // Anything not explicitly listed (future API, dotted helpers) still resolves.
    lines.push('def __getattr__(name: str) -> Any: ...');
    lines.push('');
    return lines.join('\n');
}

interface ParsedFn { name: string; params: string; }

/** Parse an API heading like `connect(a, b, [dup=-1])` into a valid Python def. */
function parseHeading(heading: string): ParsedFn | null {
    const open = heading.indexOf('(');
    const close = heading.lastIndexOf(')');
    if (open <= 0 || close < open) { return null; }

    const name = heading.slice(0, open).trim();
    // Skip object methods / attributes (jfs.*, file.*, pin.value): they're
    // reached through objects typed as Any, not module-level functions.
    if (!isValidIdentifier(name)) { return null; }

    const rawArgs = heading.slice(open + 1, close).trim();
    if (rawArgs === '') { return { name, params: '' }; }

    const params: string[] = [];
    let seenOptional = false;
    for (let token of rawArgs.split(',')) {
        token = token.trim();
        if (!token) { continue; }

        // `[name=default]` / `[name]` → optional argument.
        const optional = token.startsWith('[') && token.endsWith(']');
        if (optional) { token = token.slice(1, -1).trim(); }

        if (token.startsWith('*')) {
            // *args / **kwargs — pass through, don't force defaults after.
            params.push(token);
            continue;
        }

        const hasDefault = token.includes('=');
        if (optional || hasDefault) {
            seenOptional = true;
            params.push(hasDefault ? token : `${token}=...`);
        } else if (seenOptional) {
            // Python forbids a required arg after an optional one — make it optional.
            params.push(`${token}=...`);
        } else {
            params.push(token);
        }
    }
    return { name, params: params.join(', ') };
}

function normalize(name: string): string {
    return name.toLowerCase().replace(/-/g, '_');
}

function isValidIdentifier(s: string): boolean {
    return /^[A-Za-z_][A-Za-z0-9_]*$/.test(s);
}

function escapeDocstring(doc: string): string {
    return doc.replace(/\\/g, '\\\\').replace(/"""/g, '\\"\\"\\"').replace(/\r?\n/g, ' ').trim();
}

