/**
 * Write typings/builtins.pyi = (active language server's stdlib builtins) +
 * the Jumperless globals layered on top. Returns true on success.
 *
 * Pyright/Pylance has no "extend builtins" setting — a stub-path builtins.pyi
 * fully *replaces* the bundled one. So we copy the server's own builtins (kept
 * version-matched) and prepend the firmware globals. This makes bare names
 * resolve project-wide while keeping the full standard library intact.
 */
function writeGlobalBuiltins(typingsDir: string): boolean {
    const stdlibBuiltins = findStdlibBuiltins();
    if (!stdlibBuiltins) { return false; }

    let body: string;
    try {
        body = fs.readFileSync(stdlibBuiltins, 'utf-8');
    } catch {
        return false;
    }

    const preamble =
        GENERATED_HEADER +
        '# typeshed `builtins` (copied from the active language server) + the\n' +
        '# Jumperless firmware globals, so scripts resolve API names without imports.\n' +
        'from jumperless import *\n' +
        '\n' +
        '# The firmware also imports these into the script namespace at startup.\n' +
        'import time as time\n' +
        'import os as os\n' +
        'import math as math\n' +
        'import gc as gc\n' +
        '\n' +
        '# ---- begin vendored typeshed builtins ----\n';

    fs.writeFileSync(path.join(typingsDir, 'builtins.pyi'), preamble + body, 'utf-8');
    return true;
}

/** Find the bundled stdlib/builtins.pyi of whichever Python analyzer is active. */
function findStdlibBuiltins(): string | null {
    const ids = [
        'anysphere.cursorpyright',     // Cursor
        'ms-python.vscode-pylance',    // VSCode
        'detachhead.basedpyright',     // basedpyright users
    ];
    const rel = path.join('dist', 'typeshed-fallback', 'stdlib', 'builtins.pyi');
    for (const id of ids) {
        const ext = vscode.extensions.getExtension(id);
        if (!ext) { continue; }
        const p = path.join(ext.extensionPath, rel);
        if (fs.existsSync(p)) { return p; }
    }
    return null;
}

const TIME_STUB =
    GENERATED_HEADER +
    '"""MicroPython `time` extras for editor tooling (not uploaded to the board).\n' +
    '\n' +
    "CPython's `time` stub lacks ticks_*/sleep_ms/sleep_us. This shadows it with\n" +
    'the MicroPython surface; the __getattr__ fallback keeps the rest as Any."""\n' +
    '\n' +
    'from typing import Any\n' +
    '\n' +
    'def sleep(seconds: float) -> None: ...\n' +
    'def sleep_ms(ms: int) -> None: ...\n' +
    'def sleep_us(us: int) -> None: ...\n' +
    'def ticks_ms() -> int: ...\n' +
    'def ticks_us() -> int: ...\n' +
    'def ticks_cpu() -> int: ...\n' +
    'def ticks_add(ticks: int, delta: int) -> int: ...\n' +
    'def ticks_diff(ticks1: int, ticks2: int) -> int: ...\n' +
    '\n' +
    'def __getattr__(name: str) -> Any: ...\n';

