function writePyrightConfig(root: string, suppressUndefined: boolean): void {
    const file = path.join(root, 'pyrightconfig.json');
    let config: Record<string, any> = {};
    if (fs.existsSync(file)) {
        try { config = JSON.parse(fs.readFileSync(file, 'utf-8')); } catch { config = {}; }
    }
    config.pythonVersion = config.pythonVersion || '3.11';
    config.typeCheckingMode = config.typeCheckingMode || 'basic';
    config.stubPath = 'typings';
    config.extraPaths = mergeUnique(config.extraPaths, ['typings', '.']);
    config.reportMissingImports = 'none';
    config.reportMissingModuleSource = 'none';
    // Older Pyright/Pylance builds reject this key ("unrecognized setting"); make
    // sure we never leave it behind from a previous version of this command.
    delete config.reportShadowedImports;
    if (suppressUndefined) {
        config.reportUndefinedVariable = 'none';
    }
    fs.writeFileSync(file, JSON.stringify(config, null, 2) + '\n', 'utf-8');
}

/** Best-effort: only touch settings.json if it's missing or parses cleanly,
 *  so we never destroy a user's commented settings file. */
function writeVscodeSettings(root: string, suppressUndefined: boolean): void {
    const dir = path.join(root, '.vscode');
    const file = path.join(dir, 'settings.json');

    let settings: Record<string, any> = {};
    if (fs.existsSync(file)) {
        const raw = fs.readFileSync(file, 'utf-8');
        try {
            settings = JSON.parse(raw);
        } catch {
            // Has comments / is invalid JSON — leave it alone, pyrightconfig.json covers us.
            return;
        }
    } else {
        fs.mkdirSync(dir, { recursive: true });
    }

    settings['python.analysis.typeCheckingMode'] = settings['python.analysis.typeCheckingMode'] || 'basic';
    settings['python.analysis.extraPaths'] = mergeUnique(settings['python.analysis.extraPaths'], ['typings', '.']);
    settings['python.analysis.stubPath'] = 'typings';
    settings['python.analysis.diagnosticSeverityOverrides'] = {
        ...(settings['python.analysis.diagnosticSeverityOverrides'] || {}),
        reportMissingModuleSource: 'none',
        reportMissingImports: 'none',
        ...(suppressUndefined ? { reportUndefinedVariable: 'none' } : {}),
    };
    fs.writeFileSync(file, JSON.stringify(settings, null, 4) + '\n', 'utf-8');
}

function mergeUnique(existing: any, additions: string[]): string[] {
    const out = Array.isArray(existing) ? existing.filter((x) => typeof x === 'string') : [];
    for (const a of additions) {
        if (!out.includes(a)) { out.push(a); }
    }
    return out;
}
