function writePyrightConfig(root: string, suppressUndefined: boolean): void {
    const file = path.join(root, 'pyrightconfig.json');
    // JSONC-safe: pyright allows comments in its config, and editing as text
    // preserves them (JSON.parse would fail and we'd clobber the user's file).
    let text = fs.existsSync(file) ? fs.readFileSync(file, 'utf-8') : '{}\n';
    const current = jsoncParse(text);

    text = jsoncSet(text, 'pythonVersion', current.pythonVersion || '3.11');
    text = jsoncSet(text, 'typeCheckingMode', current.typeCheckingMode || 'basic');
    text = jsoncSet(text, 'stubPath', 'typings');
    text = jsoncSet(text, 'extraPaths', mergeUnique(current.extraPaths, ['typings', '.']));
    text = jsoncSet(text, 'reportMissingImports', 'none');
    text = jsoncSet(text, 'reportMissingModuleSource', 'none');
    // Older Pyright/Pylance builds reject this key ("unrecognized setting"); make
    // sure we never leave it behind from a previous version of this command.
    text = jsoncSet(text, 'reportShadowedImports', undefined);
    if (suppressUndefined) {
        text = jsoncSet(text, 'reportUndefinedVariable', 'none');
    }
    fs.writeFileSync(file, text, 'utf-8');
}

/** JSONC-safe: edits .vscode/settings.json as text, preserving comments. */
function writeVscodeSettings(root: string, suppressUndefined: boolean): void {
    const dir = path.join(root, '.vscode');
    const file = path.join(dir, 'settings.json');

    let text = '{}\n';
    if (fs.existsSync(file)) {
        text = fs.readFileSync(file, 'utf-8');
    } else {
        fs.mkdirSync(dir, { recursive: true });
    }
    const settings = jsoncParse(text);

    for (const prefix of analyzerPrefixes()) {
        text = jsoncSet(text, `${prefix}.typeCheckingMode`, settings[`${prefix}.typeCheckingMode`] || 'basic');
        text = jsoncSet(text, `${prefix}.extraPaths`, mergeUnique(settings[`${prefix}.extraPaths`], ['typings', '.']));
        text = jsoncSet(text, `${prefix}.stubPath`, 'typings');
        text = jsoncSet(text, `${prefix}.diagnosticSeverityOverrides`, {
            ...(settings[`${prefix}.diagnosticSeverityOverrides`] || {}),
            reportMissingModuleSource: 'none',
            reportMissingImports: 'none',
            ...(suppressUndefined ? { reportUndefinedVariable: 'none' } : {}),
        });
    }
    fs.writeFileSync(file, text, 'utf-8');
}

function mergeUnique(existing: any, additions: string[]): string[] {
    const out = Array.isArray(existing) ? existing.filter((x) => typeof x === 'string') : [];
    for (const a of additions) {
        if (!out.includes(a)) { out.push(a); }
    }
    return out;
}
